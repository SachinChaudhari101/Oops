package StarPattern;

public class NumerPattern1
{
	public static void main(String[] args) 
	{
		
		//***    111           123
		//***    222           123
		
		for(int i=1; i<=2; i++)
		{
			for(int j=1; j<=3; j++)
			{
				System.out.print(j);
			}		
			System.out.println();
		}

	}

}